<template>
    <div class="login-page">
        <div class="login-container">
            <div class="login-slideshow">
                <img class="login-bg" src="../../assets/bg_1.png" alt="">
            </div>
            <div class="login-main">
                <div class="logo">
                    <img class="login-log" src="../../assets/logo.png" alt="">
                </div>
                <div class="perspective-box">
                    <div class="box-container">
                        <h2 class="title">Docker Simple Platform</h2>
                        <h4 class="subtitle">简易云平台</h4>
                        <hr class="hr-gradient">
                        <el-form :model="ruleForm2" status-icon :rules="rules2" ref="ruleForm2" label-width="100px" class="demo-ruleForm">
                            <el-form-item label="用户名" prop="name" >
                                <el-input v-model.number="ruleForm2.name"></el-input>
                            </el-form-item>
                            <el-form-item label="密码" prop="pass">
                                <el-input type="password" v-model="ruleForm2.pass" auto-complete="off"></el-input>
                            </el-form-item>                           
                            <el-form-item>
                                <el-button type="primary" @click="submitForm('ruleForm2')">登录</el-button>
                                <el-button @click="resetForm('ruleForm2')">重置</el-button>
                            </el-form-item>
                        </el-form>
                    </div>
                </div>
            </div>
            <footer class="login-footer">Docker Simple Platform 1.0 | Docker 简易云平台</footer>
        </div>
    </div>
</template>

<script>
    

 export default {
    data() {
      var checkName = (rule, value, callback) => {
        if (!value) {
          return callback(new Error('用户名不能为空'));
        }
      };
      var validatePass = (rule, value, callback) => {
        if (value === '') {
          callback(new Error('请输入密码'));
        } else {
          if (this.ruleForm2.checkPass !== '') {
            this.$refs.ruleForm2.validateField('checkPass');
          }
          callback();
        }
      };
      return {
        ruleForm2: {
          name: '',
          pass: ''
        },
        rules2: {
          name: [
            { validator: checkName, trigger: 'blur' }
          ],
          pass: [
            { validator: validatePass, trigger: 'blur' }
          ]
        }
      };
    },
    methods: {
      submitForm(formName) {
        this.$refs[formName].validate((valid) => {
          if (valid) {
            alert('submit!');
          } else {
            console.log('error submit!!');
            return false;
          }
        });
      },
      resetForm(formName) {
        this.$refs[formName].resetFields();
      }
    }
  }
</script>


